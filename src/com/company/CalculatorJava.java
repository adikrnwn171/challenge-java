package com.company;

public class CalculatorJava {

    public static int add(int a, int b) {
        return a + b;
    }

    public static int min(int a, int b) {
        return a - b;
    }

    public static int multiply(int a, int b) {
        return a * b;
    }

    public static int divided(int a, int b) {
        return a / b;
    }
}
